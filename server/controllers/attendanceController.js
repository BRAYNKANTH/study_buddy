const db = require('../config/db');

// Mark Attendance (Tutor scans Student QR)
const markAttendance = async (req, res) => {
    let { studentId, subjectId, status, sessionId } = req.body;
    const teacherId = req.user.id;

    // SECURITY: Ignore client date. Use Server Date.
    const now = new Date();
    const serverDate = now.toISOString().split('T')[0];

    if (!studentId || !subjectId) {
        return res.status(400).json({ message: "Missing required fields" });
    }

    try {
        // 1. Verify Student Exists
        const [student] = await db.query("SELECT * FROM Student WHERE StudentID = ?", [studentId]);
        if (student.length === 0) return res.status(404).json({ message: "Student not found" });

        // 2. Find or Create Session
        // Check for 'virtual' session ID (generated by frontend/backend for timetable slots)
        if (sessionId && !sessionId.toString().startsWith('virtual_')) {
            // Validate Session Matches Subject
            const [explicitSession] = await db.query(`
                SELECT s.* FROM Session s
                JOIN SubjectGrade sg ON s.SubjectGradeID = sg.SubjectGradeID
                WHERE s.SessionID = ? AND s.TeacherID = ? AND sg.SubjectID = ?
            `, [sessionId, teacherId, subjectId]);

            if (explicitSession.length === 0) {
                return res.status(404).json({ message: "Invalid Session ID or Session mismatch." });
            }
        }
        else {
            // CASE B: Implicit / Quick Scan OR Virtual Session Confirmation
            const studentGrade = student[0].Grade;

            // Find existing session for Today + Subject + Grade
            const [existingSession] = await db.query(`
                SELECT s.* FROM Session s
                JOIN SubjectGrade sg ON s.SubjectGradeID = sg.SubjectGradeID
                WHERE s.TeacherID = ? AND sg.SubjectID = ? AND sg.Grade = ? AND s.Date = ?
            `, [teacherId, subjectId, studentGrade, serverDate]);

            if (existingSession.length > 0) {
                sessionId = existingSession[0].SessionID;
            } else {
                // Create new Session for TODAY
                console.log(`Creating ad-hoc session for ${serverDate}`);

                // Need SubjectGradeID
                const [sgRows] = await db.query("SELECT SubjectGradeID FROM SubjectGrade WHERE SubjectID = ? AND Grade = ?", [subjectId, studentGrade]);
                if (sgRows.length === 0) return res.status(400).json({ message: "No Subject-Grade found for this student's grade." });
                const subjectGradeId = sgRows[0].SubjectGradeID;

                sessionId = 'SE' + Date.now().toString().slice(-6);
                await db.query(
                    "INSERT INTO Session (SessionID, TeacherID, SubjectGradeID, Date, StartTime, EndTime) VALUES (?, ?, ?, ?, ?, ?)",
                    [sessionId, teacherId, subjectGradeId, serverDate, '00:00:00', '00:00:00']
                );
            }
        }

        // 3. Mark Attendance
        // Check if already marked for THIS session
        const [existingAttendance] = await db.query(
            "SELECT * FROM Attendance WHERE SessionID = ? AND StudentID = ?",
            [sessionId, studentId]
        );

        if (existingAttendance.length > 0) {
            return res.status(400).json({ message: "Attendance already marked for this session." });
        }

        const attendanceId = 'AT' + Date.now().toString().slice(-6);
        await db.query(
            "INSERT INTO Attendance (AttendanceID, SessionID, StudentID, Status, ScannedAt) VALUES (?, ?, ?, ?, NOW())",
            [attendanceId, sessionId, studentId, status || 'Present']
        );

        res.json({
            message: "Attendance marked successfully",
            studentName: student[0].StudentName,
            subjectId: subjectId,
            sessionId: sessionId,
            date: serverDate
        });

    } catch (err) {
        console.error("Mark Attendance Error:", err.message, err.stack);
        res.status(500).json({ message: "Server Error marking attendance", error: err.message });
    }
};

