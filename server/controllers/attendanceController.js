// NEW: Get Sessions for a Specific Class (Subject + Grade) - For Attendance Tab
// Includes BOTH Explicit Sessions and Virtual Timetable Slots
const getClassSessions = async (req, res) => {
    const { subjectId, grade } = req.params;
    const teacherId = req.user.id;

    try {
        // 1. Fetch Explicit Sessions (Manual or Attended)
        const [explicitSessions] = await db.query(`
            SELECT s.* 
            FROM Session s
            JOIN SubjectGrade sg ON s.SubjectGradeID = sg.SubjectGradeID
            WHERE sg.SubjectID = ? AND sg.Grade = ? AND s.TeacherID = ?
            ORDER BY s.Date ASC, s.StartTime ASC
        `, [subjectId, grade, teacherId]);

        // 2. Fetch Timetable Entries
        const [timetable] = await db.query(`
            SELECT t.*
            FROM Timetable t
            JOIN SubjectGrade sg ON t.SubjectGradeID = sg.SubjectGradeID
            WHERE sg.SubjectID = ? AND sg.Grade = ? AND t.TeacherID = ?
        `, [subjectId, grade, teacherId]);

        // 3. Generate Virtual Sessions for Current Month
        const combinedSessions = [...explicitSessions];

        if (timetable.length > 0) {
            const today = new Date();
            const startOfMonth = new Date(today.getFullYear(), today.getMonth(), 1);
            const endOfMonth = new Date(today.getFullYear(), today.getMonth() + 1, 0);

            const dayMap = { 'Sunday': 0, 'Monday': 1, 'Tuesday': 2, 'Wednesday': 3, 'Thursday': 4, 'Friday': 5, 'Saturday': 6 };

            // Loop through each day of the month
            for (let d = new Date(startOfMonth); d <= endOfMonth; d.setDate(d.getDate() + 1)) {
                const dayName = d.toLocaleDateString('en-US', { weekday: 'long' });
                const dateStr = d.toISOString().split('T')[0];

                // Check for timetable slots for this day
                const slots = timetable.filter(t => t.DayOfWeek === dayName);

                slots.forEach(slot => {
                    // Check if explicit session already exists for this Date + StartTime (approx check)
                    const exists = explicitSessions.some(es => es.Date.toISOString().split('T')[0] === dateStr); // Simple check: one session per day per class assumption? 
                    // Better check: allow multiple times? Use StartTime check?
                    // Assuming Timetable is the source of truth for "Planned", Session is "Actual".
                    // If an Actual session exists for that DATE, we assume it replaces the Timetable slot(s) OR is the realization of it.
                    // Let's assume strict uniqueness on Date to avoid clutter, as per simplified requirements.

                    if (!exists) {
                        combinedSessions.push({
                            SessionID: `virtual_${dateStr}_${slot.StartTime}`, // Virtual ID
                            TeacherID: teacherId,
                            SubjectGradeID: slot.SubjectGradeID, // Propagate
                            Date: new Date(dateStr), // ensure Date object
                            StartTime: slot.StartTime,
                            EndTime: slot.EndTime,
                            IsVirtual: true // Marker
                        });
                    }
                });
            }
        }

        // 4. Sort Combined List
        combinedSessions.sort((a, b) => new Date(a.Date) - new Date(b.Date) || a.StartTime.localeCompare(b.StartTime));

        res.json(combinedSessions);
    } catch (err) {
        console.error(err);
        res.status(500).json({ message: "Error fetching class sessions" });
    }
};
