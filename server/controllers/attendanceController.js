// NEW: Get Teacher Modules (Classes) - Grouped by Subject, with available Grades
const getTeacherClasses = async (req, res) => {
    const teacherId = req.user.id;
    try {
        // Fetch Subjects, Grades, and Student Counts in one efficient query
        const [classes] = await db.query(`
            SELECT 
                s.SubjectID, 
                s.SubjectName, 
                sg.Grade, 
                COUNT(e.EnrollmentID) as StudentCount
            FROM TeacherSubject ts
            JOIN Subject s ON ts.SubjectID = s.SubjectID
            JOIN SubjectGrade sg ON s.SubjectID = sg.SubjectID
            LEFT JOIN Enrollment e ON sg.SubjectGradeID = e.SubjectGradeID
            WHERE ts.TeacherID = ?
            GROUP BY s.SubjectID, s.SubjectName, sg.Grade
            HAVING StudentCount > 0 OR sg.Grade IN (6, 7, 8, 9) -- Limit to relevant grades if count is 0
            ORDER BY s.SubjectName, sg.Grade
        `, [teacherId]);

        // Filter to ensure we only show valid grades (Optional: strict logic)
        // For now, the query returns all Subject-Grade combinations for the teacher's subjects.
        // If we want to strictly show only grades 6-9 as per seed:
        const startUpClasses = classes.filter(c => [6, 7, 8, 9].includes(c.Grade));

        res.json(startUpClasses);
    } catch (err) {
        console.error(err);
        res.status(500).json({ message: "Error fetching classes" });
    }
};

// NEW: Get Students for a specific Class (Subject + Grade)
const getClassStudents = async (req, res) => {
    const { subjectId, grade } = req.params;
    try {
        const [students] = await db.query(`
            SELECT s.StudentID, s.StudentName, s.Grade, s.ParentID, u.FullName as ParentName, pp.Phone as ParentPhone, e.EnrolledDate,
            (
                SELECT GROUP_CONCAT(sub2.SubjectName SEPARATOR ', ')
                FROM Enrollment e2
                JOIN SubjectGrade sg2 ON e2.SubjectGradeID = sg2.SubjectGradeID
                JOIN Subject sub2 ON sg2.SubjectID = sub2.SubjectID
                WHERE e2.StudentID = s.StudentID
            ) as EnrolledSubjects
            FROM Student s
            JOIN Enrollment e ON s.StudentID = e.StudentID
            JOIN SubjectGrade sg ON e.SubjectGradeID = sg.SubjectGradeID
            LEFT JOIN ParentProfile pp ON s.ParentID = pp.UserID
            LEFT JOIN User u ON s.ParentID = u.UserID
            WHERE sg.SubjectID = ? AND sg.Grade = ?
        `, [subjectId, grade]);
        res.json(students);
    } catch (err) {
        console.error(err);
        res.status(500).json({ message: "Error fetching students" });
    }
};

